package SystemCombine;

public class SCModel {
	private String s;
	private String t;
	private String simT;
	public String getS() {
		return s;
	}
	public String getT() {
		return t;
	}
	public String getSimT() {
		return simT;
	}
	public void setS(String s) {
		this.s = s;
	}
	public void setT(String t) {
		this.t = t;
	}
	public void setSimT(String simT) {
		this.simT = simT;
	}
	@Override
	public String toString() {
		return "SCModel [s=" + s + ", t=" + t + ", simT=" + simT + "]";
	}
	
	
	
	
}
